public class Karta {

    protected int Wartosc;
    protected int Kolor;

    public Karta(int Wartosc, int Kolor) {
        this.Wartosc = Wartosc;
        this.Kolor = Kolor;
    }

    public int getWartosc() {
        return Wartosc;
    }

    public int getKolor() {
        return Kolor;
    }
}
